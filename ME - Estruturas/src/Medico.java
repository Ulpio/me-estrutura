public class Medico {
    public String nome;
    public String cpf;

    public Medico(String nome,String cpf){
        this.nome = nome;
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }
    public String setNome(String nome){
        return this.nome = nome;
    }
    public String getCpf() {
        return cpf;
    }
    public String setCpf(String cpf){
        return this.cpf = cpf;
    }

    


}
